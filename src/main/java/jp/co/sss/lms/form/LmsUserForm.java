package jp.co.sss.lms.form;

public class LmsUserForm {
    public String lmsUserId;

    public String loginId;

    public String password;

    public String userName;

    public String role;

    public String adminFlg;

    public String securityAgreeFlg;

    public String passwordConfirm;

    public String currentPassword;

	public String getLmsUserId() {
		return lmsUserId;
	}

	public void setLmsUserId(String lmsUserId) {
		this.lmsUserId = lmsUserId;
	}

	public String getLoginId() {
		return loginId;
	}

	public void setLoginId(String loginId) {
		this.loginId = loginId;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getRole() {
		return role;
	}

	public void setRole(String role) {
		this.role = role;
	}

	public String getAdminFlg() {
		return adminFlg;
	}

	public void setAdminFlg(String adminFlg) {
		this.adminFlg = adminFlg;
	}

	public String getSecurityAgreeFlg() {
		return securityAgreeFlg;
	}

	public void setSecurityAgreeFlg(String securityAgreeFlg) {
		this.securityAgreeFlg = securityAgreeFlg;
	}

	public String getPasswordConfirm() {
		return passwordConfirm;
	}

	public void setPasswordConfirm(String passwordConfirm) {
		this.passwordConfirm = passwordConfirm;
	}

	public String getCurrentPassword() {
		return currentPassword;
	}

	public void setCurrentPassword(String currentPassword) {
		this.currentPassword = currentPassword;
	}
    
    
}
